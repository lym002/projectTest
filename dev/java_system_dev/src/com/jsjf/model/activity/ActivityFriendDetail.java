package com.jsjf.model.activity;

import java.math.BigDecimal;
import java.util.Date;

public class ActivityFriendDetail {

	private Integer id;
	private Integer fid;
	private Integer days;
	private Integer awardType;
	private Integer investLimit;
	private Date addDate;
	private BigDecimal amount;
	private String awardTypeString;
	private BigDecimal appReward;
	private String appRewardString;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getFid() {
		return fid;
	}

	public void setFid(Integer fid) {
		this.fid = fid;
	}

	public Integer getDays() {
		return days;
	}

	public void setDays(Integer days) {
		this.days = days;
	}

	public Integer getAwardType() {
		return awardType;
	}

	public void setAwardType(Integer awardType) {
		this.awardType = awardType;
	}

	public Integer getInvestLimit() {
		return investLimit;
	}

	public void setInvestLimit(Integer investLimit) {
		this.investLimit = investLimit;
	}

	public Date getAddDate() {
		return addDate;
	}

	public void setAddDate(Date addDate) {
		this.addDate = addDate;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getAwardTypeString() {
		return awardTypeString;
	}

	public void setAwardTypeString(String awardTypeString) {
		this.awardTypeString = awardTypeString;
	}

	public BigDecimal getAppReward() {
		return appReward;
	}

	public void setAppReward(BigDecimal appReward) {
		this.appReward = appReward;
	}

	public String getAppRewardString() {
		return appRewardString;
	}

	public void setAppRewardString(String appRewardString) {
		this.appRewardString = appRewardString;
	}
	
}
