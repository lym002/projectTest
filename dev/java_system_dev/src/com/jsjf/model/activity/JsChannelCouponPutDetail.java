package com.jsjf.model.activity;

import java.math.BigDecimal;

public class JsChannelCouponPutDetail {
	private Integer id;
	private Integer putId;
	private String mobile;
	private BigDecimal amount;
	private String code;
	private Integer status;
	private Integer channelType; //0非cps，1cps
	private String recommCodes;//推荐码
	
	
	
	public Integer getChannelType() {
		return channelType;
	}
	public void setChannelType(Integer channelType) {
		this.channelType = channelType;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public BigDecimal getAmount() {
		return amount;
	}
	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getPutId() {
		return putId;
	}
	public void setPutId(Integer putId) {
		this.putId = putId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getRecommCodes() {
		return recommCodes;
	}
	public void setRecommCodes(String recommCodes) {
		this.recommCodes = recommCodes;
	}
	
}
