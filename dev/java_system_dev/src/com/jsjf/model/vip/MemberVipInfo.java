package com.jsjf.model.vip;

import java.math.BigDecimal;

public class MemberVipInfo {
    private Integer id;

    private Integer uid;

    private BigDecimal growthValue;

    private Integer vipLevel;

    private String code;

    public MemberVipInfo() {
    }

    public MemberVipInfo(Integer id, Integer uid, BigDecimal growthValue, Integer vipLevel) {
        this.id = id;
        this.uid = uid;
        this.growthValue = growthValue;
        this.vipLevel = vipLevel;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public BigDecimal getGrowthValue() {
        return growthValue;
    }

    public void setGrowthValue(BigDecimal growthValue) {
        this.growthValue = growthValue;
    }

    public Integer getVipLevel() {
        return vipLevel;
    }

    public void setVipLevel(Integer vipLevel) {
        this.vipLevel = vipLevel;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}