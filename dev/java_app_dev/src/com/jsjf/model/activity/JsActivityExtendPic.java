package com.jsjf.model.activity;

import java.io.Serializable;
import java.util.Date;

public class JsActivityExtendPic implements Serializable{

	private Integer id; //
	private Integer extendId; //外键id
	private Integer type; //1:公益活动扩展图片
	private Integer status; //0:删除,1正常
	private String imgUrl; //图片url
	private Integer showSort; //图片顺序
	private Date addDate; //添加时间
	
	public JsActivityExtendPic(Integer id, Integer extendId, Integer type, Integer status, String imgUrl,
			Integer showSort, Date addDate) {
		super();
		this.id = id;
		this.extendId = extendId;
		this.type = type;
		this.status = status;
		this.imgUrl = imgUrl;
		this.showSort = showSort;
		this.addDate = addDate;
	}
	public JsActivityExtendPic() {
		super();
		// TODO Auto-generated constructor stub
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getExtendId() {
		return extendId;
	}
	public void setExtendId(Integer extendId) {
		this.extendId = extendId;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	public Integer getShowSort() {
		return showSort;
	}
	public void setShowSort(Integer showSort) {
		this.showSort = showSort;
	}
	public Date getAddDate() {
		return addDate;
	}
	public void setAddDate(Date addDate) {
		this.addDate = addDate;
	}
	
	
	
	
}
