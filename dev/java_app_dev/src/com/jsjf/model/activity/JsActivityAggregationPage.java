package com.jsjf.model.activity;

import java.util.Date;

public class JsActivityAggregationPage {

	private Integer id;//id
	private String title;//活动标题
	private String activityDate;//活动时间
	private String pcUrl;//pc活动链接
	private String pcPic;//pc图片
	private String appUrl;//app活动链接
	private String appPic;//app图片
	private Integer isTop;//是否置顶 0-不置顶，1-置顶
	private Integer status;//状态 0-进行中，1-已结束
	private Integer addUser;//添加人
	private Integer addTime;//添加时间
	private String updateUser;//修改人
	private Date updateTime;//修改时间
	
	
	public JsActivityAggregationPage(){
		
	}
	
	public JsActivityAggregationPage(Integer id,String title,String activityDate,String pcUrl,String pcPic,String appUrl,String appPic,Integer isTop,Integer status,Integer addUser,Integer addTime,String updateUser,Date updateTime){
		this.id = id;
		this.title = title;
		this.activityDate = activityDate;
		this.pcUrl = pcUrl;
		this.pcPic = pcPic;
		this.appUrl = appUrl;
		this.appPic = appPic;
		this.isTop = isTop;
		this.status = status;
		this.addUser = addUser;
		this.addTime = addTime;
		this.updateUser = updateUser;
		this.updateTime = updateTime;
	}


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getTitle() {
		return title;
	}


	public void setTitle(String title) {
		this.title = title;
	}


	public String getActivityDate() {
		return activityDate;
	}


	public void setActivityDate(String activityDate) {
		this.activityDate = activityDate;
	}


	public String getPcUrl() {
		return pcUrl;
	}


	public void setPcUrl(String pcUrl) {
		this.pcUrl = pcUrl;
	}


	public String getPcPic() {
		return pcPic;
	}


	public void setPcPic(String pcPic) {
		this.pcPic = pcPic;
	}


	public String getAppUrl() {
		return appUrl;
	}


	public void setAppUrl(String appUrl) {
		this.appUrl = appUrl;
	}


	public String getAppPic() {
		return appPic;
	}


	public void setAppPic(String appPic) {
		this.appPic = appPic;
	}


	public Integer getIsTop() {
		return isTop;
	}


	public void setIsTop(Integer isTop) {
		this.isTop = isTop;
	}


	public Integer getStatus() {
		return status;
	}


	public void setStatus(Integer status) {
		this.status = status;
	}


	public Integer getAddUser() {
		return addUser;
	}


	public void setAddUser(Integer addUser) {
		this.addUser = addUser;
	}


	public Integer getAddTime() {
		return addTime;
	}


	public void setAddTime(Integer addTime) {
		this.addTime = addTime;
	}


	public String getUpdateUser() {
		return updateUser;
	}


	public void setUpdateUser(String updateUser) {
		this.updateUser = updateUser;
	}


	public Date getUpdateTime() {
		return updateTime;
	}


	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	
}
