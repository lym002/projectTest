package com.jytpay.vo;

import java.io.Serializable;
import java.math.BigDecimal;

public class JYTResultData implements Serializable{
    private static final long serialVersionUID = 1L;
    private String            resp_code;     //交易返回码
    private String            resp_desc;     //交易返回码说明
    private String            ori_tran_flowid;          // 原交易流水号
    private String            account_no;                 // 银行账号
    private String            account_name;            // 银行账号名称
    private BigDecimal        tran_amt;          // 交易金额
    private String            tran_resp_code;          // 交易响应码
    private String            tran_resp_desc;           // 交易返回信息描述
    private String            tran_state;           // 交易状态
    private String            remark;          // 备注信息
    
	//四要素返回银行名称
    private String            bank_name;          //银行名称
    
	public String getResp_code() {
		return resp_code;
	}
	public void setResp_code(String resp_code) {
		this.resp_code = resp_code;
	}
	public String getResp_desc() {
		return resp_desc;
	}
	public void setResp_desc(String resp_desc) {
		this.resp_desc = resp_desc;
	}
	public String getOri_tran_flowid() {
		return ori_tran_flowid;
	}
	public void setOri_tran_flowid(String ori_tran_flowid) {
		this.ori_tran_flowid = ori_tran_flowid;
	}
	public String getAccount_no() {
		return account_no;
	}
	public void setAccount_no(String account_no) {
		this.account_no = account_no;
	}
	public String getAccount_name() {
		return account_name;
	}
	public void setAccount_name(String account_name) {
		this.account_name = account_name;
	}
	public BigDecimal getTran_amt() {
		return tran_amt;
	}
	public void setTran_amt(BigDecimal tran_amt) {
		this.tran_amt = tran_amt;
	}
	public String getTran_resp_code() {
		return tran_resp_code;
	}
	public void setTran_resp_code(String tran_resp_code) {
		this.tran_resp_code = tran_resp_code;
	}
	public String getTran_resp_desc() {
		return tran_resp_desc;
	}
	public void setTran_resp_desc(String tran_resp_desc) {
		this.tran_resp_desc = tran_resp_desc;
	}
	public String getTran_state() {
		return tran_state;
	}
	public void setTran_state(String tran_state) {
		this.tran_state = tran_state;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getBank_name() {
		return bank_name;
	}
	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}
	
}
