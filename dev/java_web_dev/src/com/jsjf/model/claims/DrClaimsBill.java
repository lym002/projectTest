package com.jsjf.model.claims;

import java.math.BigDecimal;
import java.util.Date;

public class DrClaimsBill{

    private Integer id;//票据ID  

    private Integer lid;//贷款项目基本信息ID
    
    private Integer type;//票据类型
    
    private String drawer;//开票人

    private String payee;//收款人

    private String acceptor;//承兑人
    
    private BigDecimal amount;//票据金额

    private String number;//票据号码
    
    private String endorsee;//被背书人
    
    private Date startDate;//出票日期
    
    private Date endDate;//到期日期

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getLid() {
		return lid;
	}

	public void setLid(Integer lid) {
		this.lid = lid;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getDrawer() {
		return drawer;
	}

	public void setDrawer(String drawer) {
		this.drawer = drawer;
	}

	public String getPayee() {
		return payee;
	}

	public void setPayee(String payee) {
		this.payee = payee;
	}

	public String getAcceptor() {
		return acceptor;
	}

	public void setAcceptor(String acceptor) {
		this.acceptor = acceptor;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getEndorsee() {
		return endorsee;
	}

	public void setEndorsee(String endorsee) {
		this.endorsee = endorsee;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

}