package com.jsjf.model.system;

import java.math.BigDecimal;
import java.util.Date;

public class SysActivityParameter {
    /**
     * 主键ID  
     */
    private Integer id;

    /**
     * 活动名称  
     */
    private String name;
    
    /**
     * 活动编号  
     */
    private String code;
    
    /**
     * 启动资金  
     */
    private BigDecimal amount;
    
    /**
     * 剩余资金  
     */
    private BigDecimal surplusFunds;
    
    /**
     * 开始时间  
     */
    private Date startTime;
    
    /**
     * 结束时间  
     */
    private Date endTime;
    
    /**
     * 状态: 0 : 活动有效 1：活动失效 
     */
    private Integer status;
    
    /**
     * 备注
     */
    private String remark;
    
    
    

	public SysActivityParameter() {
		
	}
	

	public SysActivityParameter(String name, String code, BigDecimal amount,
			BigDecimal surplusFunds, Date startTime, Date endTime,
			Integer status, String remark) {
		super();
		this.name = name;
		this.code = code;
		this.amount = amount;
		this.surplusFunds = surplusFunds;
		this.startTime = startTime;
		this.endTime = endTime;
		this.status = status;
		this.remark = remark;
	}


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getcode() {
		return code;
	}

	public void setcode(String code) {
		this.code = code;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public BigDecimal getSurplusFunds() {
		return surplusFunds;
	}

	public void setSurplusFunds(BigDecimal surplusFunds) {
		this.surplusFunds = surplusFunds;
	}

	public Date getStartTime() {
		return startTime;
	}

	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}

	public Date getEndTime() {
		return endTime;
	}

	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
    
    

   
}